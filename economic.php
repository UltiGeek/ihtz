                       <p>To-date 122 individuals (from 3 Districts of Dar es Salaam, Tabata-Chang’ombe, Kigamboni and Keko), have been trained on entrepreneurship and Islamic business ethics (94 women and 28 men). The demand for the same by other groups is high, an indicator that the project is a life changing program. The training motivates the beneficiaries to engage in business and enterprises and becomes a simple tool for developing business plans and a reason to source and apply for a loan from Islamic Help. A fee of 1500Tshs ($1 or £0.5) is paid by each individual for the two (2) days training (Islamic Help provides meals, stationary, costs of travel for the trainers, equipment cost etc).</p> 

                        <p>Once completed the beneficiaries are grouped and the preparation for a business plan thus starts whereby they  raise and submit their plan/proposal to Islamic Help project appraisal desk for approval for which funds are given as “Qardha-al-hassan” Loan and Grants. Follow up and repayment process is done after the disbursement of fund and delivery of project according to terms and the nature of the project.</p>
