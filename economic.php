                       <p>In Tanzania it is estimated that women especially in rural areas provide 80% of the labour force in farming and producing 60% of food production. Poverty and disempowerment go hand-in-hand; as poverty reduces, empowerment increases. In 2012 Islamic Help initiated the ‘Wezesha Maisha’ project, a livelihood project for women in economic poor and vulnerable communities. The project seeks to achieve empowerment by providing training in management and entrepreneurship.  Post training, Islamic Help provides the selected groups with interest-free loans that aim to create economic opportunities for not only the beneficiaries but also the community. </p>
