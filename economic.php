                        <h3>TREND project</h3>
                        <p>Tanzania Rural Enterprises Network for Development (TREND) is yet another project aimed at empowering the economic poor through promotion of “fair trade”. The aim of TREND is to support subsistence farmers by ensuring that they get a better market share and bargain for their products. This project will also help the community to improve the quality of their product and scale up their methods of farming, producing, packaging and delivery. Pilot projects will soon be launched for products such as honey, sesame seeds and sunflower.<p>

